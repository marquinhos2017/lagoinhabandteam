  Widget buildChordInfo(String chordName, String chordPattern) {
    return GestureDetector(
      onTap: () {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('$chordName: $chordPattern'),
          ),
        );
      },
      child: Text(
        '$chordName = $chordPattern',
        style: TextStyle(fontSize: 16.0, color: Colors.blue),
      ),
    );
  }
